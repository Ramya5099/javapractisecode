package JavaBasics;

public class NestedLoops {

	public static void main(String[] args) {
		for(int i=0; i<=9;i++)
		{
			for(int j=0; j<=9; j++)
			{
				System.out.println(i+""+j+" ");
			}
		System.out.println("      ");
		System.out.println(".......");
	}
	// do while loop
		
		int v=1;
		do
		{
			System.out.println(v);
			v++;
		}
		while(v<=10);
	}

}
